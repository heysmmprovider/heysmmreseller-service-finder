// Sort button click handler
sortButton.addEventListener('click', function() {
    if (sortOrder === 'none') {
        sortOrder = 'asc';
        sortButton.innerHTML = 'Price <span class="sort-arrow">‚Üë</span>';
        sortButton.classList.add('active');
    } else if (sortOrder === 'asc') {
        sortOrder = 'desc';
        sortButton.innerHTML = 'Price <span class="sort-arrow">‚Üì</span>';
    } else {
        sortOrder = 'none';
        sortButton.innerHTML = 'Price <span class="sort-arrow">‚Üï</span>';
        sortButton.classList.remove('active');
    }
    applyFilters();
});

// Input event listeners
searchInput.addEventListener('input', applyFilters);
minFilter.addEventListener('input', applyFilters);
maxFilter.addEventListener('input', applyFilters);

// Apply all filters
function applyFilters() {
    const searchTerm = searchInput.value.toLowerCase().trim();
    const minValue = minFilter.value ? parseInt(minFilter.value) : null;
    const maxValue = maxFilter.value ? parseInt(maxFilter.value) : null;
    
    // Filter services
    filteredServices = services.filter(service => {
        // Search filter
        const matchesSearch = searchTerm === '' || 
            service.name.toLowerCase().includes(searchTerm) ||
            service.service.includes(searchTerm) ||
            service.type.toLowerCase().includes(searchTerm);
        
        // Min/Max filters
        const serviceMin = parseInt(service.min);
        const serviceMax = parseInt(service.max);
        const matchesMin = minValue === null || serviceMin >= minValue;
        const matchesMax = maxValue === null || serviceMax <= maxValue;
        
        return matchesSearch && matchesMin && matchesMax;
    });
    
    // Sort if needed
    if (sortOrder !== 'none') {
        filteredServices.sort((a, b) => {
            const priceA = parseFloat(a.rate);
            const priceB = parseFloat(b.rate);
            return sortOrder === 'asc' ? priceA - priceB : priceB - priceA;
        });
    }
    
    displayServices(filteredServices);
}

